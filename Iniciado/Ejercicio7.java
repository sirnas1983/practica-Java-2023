package Iniciado;
// El resultado es un real ya que la operacion entre
// reales y enteros siempre va a dar como resultado
// un numero real.

public class Ejercicio7 {
    public static void main(String[] args) {
        double real1, real2;
        real1 = 50.0;
        real2 = 25.5;
        var resultado1 = real1 * real2;
        int entero1 = 25;
        int entero2 = 32;
        var resultado2 = entero1 * entero2;
        System.out.println(resultado1 * resultado2);
    }
}
